#include <string.h>

int	ft_strncmp(char *s1, char *s2, unsigned int n)
{
	int i;

	i = 0;
	while (*s1 != '\0' && (*s1 == *s2) && n > 0)
	{
		++i;
		n = n - 1;
	}
	return (s1[i] - s2[i]);
}
