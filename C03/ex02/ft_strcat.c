#include <string.h>

char	*ft_strcat(char *dest, char *src)
{
	char	*pop;

	pop = dest;
	while (*pop != '\0')
		pop++;
	while (*src != '\0')
	{
		*pop = *src;
		pop++;
		src++;
	}
	*pop = '\0';
	return (dest);
}
