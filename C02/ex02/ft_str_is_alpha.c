#include <unistd.h>
#include <stdio.h>

int	ft_str_is_alpha(char *str)
{
	int		k;
	int		a;
	char	j;

	a = 0;
	k = 0;
	while (str[a] != '\0')
	{
		j = str[a];
		if (!((j >= 'A' && j <= 'Z') || (j >= 'a' && j <= 'z')))
		{
			++k;
			break ;
		}
		++a;
	}
	if (k > 0)
	{
		a = 0;
	}
	else
	{
		a = 1;
	}
	return (a);
}
