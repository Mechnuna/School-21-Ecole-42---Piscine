#include <unistd.h>
#include <stdio.h>

char	*ft_strupcase(char *str)
{
	int		a;
	char	j;

	a = 0;
	while (str[a] != '\0')
	{
		j = str[a];
		if (j >= 'a' && j <= 'z')
		{
			str[a] = j - 32;
		}
		++a;
	}
	return (str);
}
