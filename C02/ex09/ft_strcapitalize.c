char	*ft_strcapitalize(char *str)
{
	int	a;
	char	j;
	char	p;

	a = 0;
	p = 0;
	while (str[a] != '\0')
	{
		j = str[a];
		while (!(j >= 'A' && j <= 'Z'))
		{
			if (j == ' ')
			{
				++p;
			}
			if (p > 0)
			{
				str[a] = j - 32;
			}
		}
		++a;
		p = 0;
	}
	return (str);
}
