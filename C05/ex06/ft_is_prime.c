int	ft_is_prime(int nb)
{
	int a;
	int b;

	a = 1;
	b = 0;
	if (nb < 2)
		return (0);
	if (nb == 2147483647)
		return (1);
	while (a <= nb)
	{
		if (nb % a == 0)
		{
			++b;
		}
		++a;
	}
	if (b > 2)
		return (0);
	else
		return (1);
}
