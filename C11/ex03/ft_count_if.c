int		ft_count_if(char **tab, int length, int (*f)(char*))
{
	int a;
	int b;

	a = 0;
	b = 0;
	while (a < length)
	{
		if ((*f)(tab[a]) != 0)
			++b;
		++a;
	}
	return (b);
}
