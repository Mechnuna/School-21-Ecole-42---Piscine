int		ft_is_sort(int *tab, int length, int (*f)(int, int))
{
	int a;
	int b;

	a = 0;
	b = 0;
	while (a < length - 1)
	{
		if ((*f)(tab[a], tab[a + 1]) > 0)
			++b;
		++a;
	}
	if (b == (length - 1))
		return (1);
	if (b > 0)
		return (0);
	else
		return (1);
}
